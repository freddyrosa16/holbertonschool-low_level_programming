/**
 * _abs - function to find the absolute value
 * @n: variable for numbers in the absolute value
 * Return: return value of the same number or multiply by -1
 */
int _abs(int n)
{
	if (n < 0)
	{
		return (-n);
	}
	else
	{
		return (n);
	}
}




